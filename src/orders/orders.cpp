#include <utils/mmbot_strong_types.hpp>

#include "orders.hpp"

namespace antara::orders
{
    bool order_level::operator==(const order_level &other) const
    {
        return price.value() == other.price.value()
               && quantity == other.quantity
               && side == other.side;
    }
}
